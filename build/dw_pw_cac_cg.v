module dw_pw_cac_cg #(  parameter IN_DATA_W = 8,
  parameter DW_COEFF_W = 8,
  parameter DW_K_H = 3,
  parameter DW_K_W = 3,
  parameter DW_MUL_W = IN_DATA_W+DW_COEFF_W,
  parameter DW_SUM_W = DW_MUL_W+$clog2(DW_K_H*DW_K_W),
  parameter DW_COL = 5,
  parameter DW_ROW = 25,
  parameter DW_STRIDE = 1,
  parameter DW_PAD_TOP = (DW_K_H-1)/2,
  parameter DW_PAD_BOTTOM = (DW_K_H)/2,
  parameter DW_PAD_LEFT = (DW_K_W-1)/2,
  parameter DW_PAD_RIGHT = (DW_K_W)/2,
  parameter DW_COEFF_GRP_NUM = 64*4,
  parameter DW_FRAME_GRP_NUM = 64,
  parameter DW_MAC_PIPELINE = 1,
  parameter DW_COEFF_INIT_FILE = "",
  parameter DW_BIAS_INIT_FILE = "",
  parameter DW_OUT_WIDTH = 8,
  parameter DW_SHIFT_VAL = 16,
  parameter DW_BIAS_GROUP_BITS = 2,
  parameter DW_BIAS_GROUP_SIZE = 4*1,
  parameter DW_BIAS_CH_BITS = 6,
  parameter DW_MULT_CNT = 4,
  parameter [11:0] DW_MULT_FACTOR0 = 12'sd1246,
  parameter [11:0] DW_MULT_FACTOR1 = 12'sd828,
  parameter [11:0] DW_MULT_FACTOR2 = 12'sd412,
  parameter [11:0] DW_MULT_FACTOR3 = 12'sd512,
  parameter DW_FIFO_DEPTH = 16,
  parameter DW_FIFO_AF_LEVEL = 10,
  parameter PW_OUT_CH = 64,
  parameter PW_COEFF_W = 8,
  parameter PW_K_H = 1,
  parameter PW_K_W = 1,
  parameter PW_MUL_W = DW_OUT_WIDTH+PW_COEFF_W,
  parameter PW_SUM_W = PW_MUL_W+$clog2(PW_K_H*PW_K_W),
  parameter PW_COEFF_GRP_NUM = 64,
  parameter PW_FRAME_GRP_NUM = 64,
  parameter PW_MAC_PIPELINE = 1,
  parameter PW_COEFF_INIT_FILE = "",
  parameter PW_FIFO_DEPTH = 4,
  parameter PW_FIFO_AF_LEVEL = 2,
  parameter RAM_ONE_DATA_W = PW_SUM_W+$clog2(PW_OUT_CH),
  parameter SEGMENTS = 4,
  parameter PIXEL_DEPTH = ((DW_COL+DW_PAD_LEFT+DW_PAD_RIGHT-DW_K_W)/DW_STRIDE+1)*((DW_ROW+DW_PAD_TOP+DW_PAD_BOTTOM-DW_K_H)/DW_STRIDE+1),
  parameter RAM_ADDR_W = $clog2(PIXEL_DEPTH*SEGMENTS),
  parameter IN_FRAME_SIZE = 64
)
(
input wire  [IN_DATA_W-1:0] in_pixel,
output wire  [RAM_ADDR_W-1:0] ram_raddr,
output wire  [RAM_ADDR_W-1:0] ram_waddr,
input wire in_valid,
input wire rst_n,
input wire clk,
input wire in_end_all_frame,
input wire [(PW_OUT_CH/SEGMENTS)*RAM_ONE_DATA_W-1:0] ram_rdata,
output wire in_ready,
output wire ram_re,
output wire [(PW_OUT_CH/SEGMENTS)*RAM_ONE_DATA_W-1:0] ram_wdata,
output wire ram_we,
output wire end_all_frame,
output wire end_frame
);

  //----Code starts here: integrated by Robei-----
  
   localparam RAM_DATA_W = (PW_OUT_CH/SEGMENTS) * RAM_ONE_DATA_W;
   wire                                 pw_mult_valid;
   wire                                 pw_mult_ready;
   wire                                 pw_mult_end_frame;
   wire                                 pw_mult_end_all_frame;
   wire signed [IN_DATA_W-1:0]          in_pixel_s = $signed(in_pixel);
   wire signed [PW_OUT_CH*PW_SUM_W-1:0] pw_mult_data_bus;
  
   dw2pw_mult #(
    .IN_DATA_W(IN_DATA_W),
    .DW_COEFF_W(DW_COEFF_W),
    .DW_K_H(DW_K_H),
    .DW_K_W(DW_K_W),
    .DW_MUL_W(DW_MUL_W),
    .DW_SUM_W(DW_SUM_W),
    .DW_COL(DW_COL),
    .DW_ROW(DW_ROW),
    .DW_STRIDE(DW_STRIDE),
    .DW_PAD_TOP(DW_PAD_TOP),
    .DW_PAD_BOTTOM(DW_PAD_BOTTOM),
    .DW_PAD_LEFT(DW_PAD_LEFT),
    .DW_PAD_RIGHT(DW_PAD_RIGHT),
    .DW_COEFF_GRP_NUM(DW_COEFF_GRP_NUM),
    .DW_FRAME_GRP_NUM(DW_FRAME_GRP_NUM),
    .DW_MAC_PIPELINE(DW_MAC_PIPELINE),
    .DW_COEFF_INIT_FILE(DW_COEFF_INIT_FILE),
    .DW_OUT_WIDTH(DW_OUT_WIDTH),
    .DW_SHIFT_VAL(DW_SHIFT_VAL),
    .DW_BIAS_GROUP_BITS(DW_BIAS_GROUP_BITS),
    .DW_BIAS_GROUP_SIZE(DW_BIAS_GROUP_SIZE),
    .DW_BIAS_CH_BITS(DW_BIAS_CH_BITS),
    .DW_BIAS_INIT_FILE(DW_BIAS_INIT_FILE),
    .DW_MULT_CNT(DW_MULT_CNT),
    .DW_MULT_FACTOR0(DW_MULT_FACTOR0),
    .DW_MULT_FACTOR1(DW_MULT_FACTOR1),
    .DW_MULT_FACTOR2(DW_MULT_FACTOR2),
    .DW_MULT_FACTOR3(DW_MULT_FACTOR3),
    .DW_FIFO_DEPTH(DW_FIFO_DEPTH),
    .DW_FIFO_AF_LEVEL(DW_FIFO_AF_LEVEL),
    .PW_OUT_CH(PW_OUT_CH),
    .PW_COEFF_W(PW_COEFF_W),
    .PW_K_H(PW_K_H),
    .PW_K_W(PW_K_W),
    .PW_MUL_W(PW_MUL_W),
    .PW_SUM_W(PW_SUM_W),
    .PW_COEFF_GRP_NUM(PW_COEFF_GRP_NUM),
    .PW_FRAME_GRP_NUM(PW_FRAME_GRP_NUM),
    .PW_MAC_PIPELINE(PW_MAC_PIPELINE),
    .PW_COEFF_INIT_FILE(PW_COEFF_INIT_FILE),
    .PW_FIFO_DEPTH(PW_FIFO_DEPTH),
    .PW_FIFO_AF_LEVEL(PW_FIFO_AF_LEVEL)
   ) u_dw2pw_mult (
    .clk(clk),
    .rst_n(rst_n),
    .in_valid(in_valid),
    .in_ready(in_ready),
    .in_pixel(in_pixel_s),
    .in_end_all_frame(in_end_all_frame),
    .pw_mult_valid(pw_mult_valid),
    .pw_mult_ready(pw_mult_ready),
    .pw_mult_end_frame(pw_mult_end_frame),
    .pw_mult_end_all_frame(pw_mult_end_all_frame),
    .pw_mult_data_bus(pw_mult_data_bus)
   );
  
   pw_sum #(
    .MULT_WIDTH(PW_SUM_W),
    .RAM_DATA_W(RAM_DATA_W),
    .CHANNELS(PW_OUT_CH),
    .SEGMENTS(SEGMENTS),
    .PIXEL_DEPTH(PIXEL_DEPTH),
    .RAM_ADDR_W(RAM_ADDR_W),
    .IN_FRAME_SIZE(IN_FRAME_SIZE)
   ) u_pw_sum (
    .clk(clk),
    .rst_n(rst_n),
    .in_valid(pw_mult_valid),
    .in_ready(pw_mult_ready),
    .mult_in(pw_mult_data_bus),
    .ram_re(ram_re),
    .ram_raddr(ram_raddr),
    .ram_rdata(ram_rdata),
    .ram_we(ram_we),
    .ram_waddr(ram_waddr),
    .ram_wdata(ram_wdata),
    .end_frame(end_frame),
    .end_all_frame(end_all_frame)
   );
  
